package com.company;

public class calculator {
    public static class Calculator{
        public static float plus(float x,float y){
            return x+y;
        }
        public static float minus(float x,float y){
            return x-y;
        }
        public static float bara(float x,float y){
            return x/y;
        }
        public static float stea(float x,float y){
            return x*y;
        }
    }
}
